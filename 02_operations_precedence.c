#include<stdio.h>
#include<math.h>

int main(){

    int x = 4;
    int y = 3;

    printf("The value of 3*x - 8*y is %d \n", 3*x - 8*y);
    printf("The value of 3*x / 8*y is %d \n", 3*x / 8*y);
    printf("The value of 8*y / 3*x is %d \n", 8*y / 3*x);

    printf("The value of 3^3 is %f \n", pow(3, 3));

    return 0;
}